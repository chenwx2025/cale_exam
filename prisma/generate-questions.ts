async function isDuplicate(question: string, categoryId: string): Promise<boolean> {
  const existingQuestions = await prisma.question.findMany({
    where: { categoryId },
    select: { question: true }
  })

  // ç®€å•çš„é‡å¤æ£€æµ‹ï¼šå»é™¤ç©ºæ ¼å’Œæ ‡ç‚¹åæ¯”è¾ƒ
  const normalizedNew = question.replace(/[^\w\u4e00-\u9fa5]/g, '').toLowerCase()

  for (const existing of existingQuestions) {
    const normalizedExisting = existing.question.replace(/[^\w\u4e00-\u9fa5]/g, '').toLowerCase()

    // å¦‚æœç›¸ä¼¼åº¦è¶…è¿‡80%ï¼Œè®¤ä¸ºæ˜¯é‡å¤
    const similarity = calculateSimilarity(normalizedNew, normalizedExisting)
    if (similarity > 0.8) {
      return true
    }
  }

  return false
}

// è®¡ç®—ä¸¤ä¸ªå­—ç¬¦ä¸²çš„ç›¸ä¼¼åº¦ï¼ˆç®€å•çš„Levenshteinè·ç¦»ï¼‰
function calculateSimilarity(str1: string, str2: string): number {
  const len1 = str1.length
  const len2 = str2.length

  if (len1 === 0) return len2 === 0 ? 1 : 0
  if (len2 === 0) return 0

  const matrix: number[][] = []

  for (let i = 0; i <= len1; i++) {
    matrix[i] = [i]
  }

  for (let j = 0; j <= len2; j++) {
    matrix[0][j] = j
  }

  for (let i = 1; i <= len1; i++) {
    for (let j = 1; j <= len2; j++) {
      const cost = str1[i - 1] === str2[j - 1] ? 0 : 1
      matrix[i][j] = Math.min(
        matrix[i - 1][j] + 1,
        matrix[i][j - 1] + 1,
        matrix[i - 1][j - 1] + cost
      )
    }
  }

  const distance = matrix[len1][len2]
  const maxLen = Math.max(len1, len2)
  return 1 - (distance / maxLen)
}

